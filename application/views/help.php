                            <h3 class="post-title">使用帮助</h3>
                            <div class="post-content">
								<p align="justify">　　临沂市政府</a>出台《关于加快电子商务发展的意见》，明确了“临沂版”24条措施力促电子商务发展，并指出自2015年起三年内，每年拿出1亿元电子商务发展专项资金后，近日，临沂1亿元扶持资金的“花钱”指针《临沂市电子商务发展专项资金管理暂行办法》出炉了，有效期至2016年12月4日。想必电商“朋友们”从中也能找出今后发展的方向。</p>
								<p align="justify">　　据了解，该笔电商发展专项资金，由市级财政预算安排，主要用于构建电子商务产业体系、提高电子商务应用水平、完善电子商务支撑和服务体系等方面，由市财政局、市商务局等有关主管部门按照职责分工共同管理。</p>
								<p align="justify"><strong>　　哪些对象可以申请专项资金？</strong></p>
								<p align="justify">　　办法明确规定，须是在临沂依法注册、具有独立法人资格的从事电子商务运营、应用及专业配套服务的企业和相关行业机构。具体包括：</p>
								<p align="justify">　　1、电子商务平台，需在互联网上开展电子商务应用服务且已取得互联网信息服务增值电信业务经营许可证，或已通过非经营性和互联网信息服务备案，取得ICP证号。</p>
								<p align="justify">　　2、电子商务服务业。致力于为企业(包括个人)开展电子商务应用提供第三方公共服务，且能够在信息、订单、支付、物流、培训等方面为电子商务应用企业提供服务的电子商务专业配套服务企业和机构。</p>
								<p align="justify">　　3、电子商务应用企业。加入第三方平台实现网上交易的企业、自建或共建电子商务营销渠道的企业。</p>
								<p align="justify">　　4、电子商务重点项目。重点电子商务产业集聚园区、电子商务平台建设项目，获得国家、省支持开展的电子商务项目、重点领域电子商务基础设施项目、电子商务规制建设研究项目、拓展国内外<a title="null" target="_blank" href="http://www.langya.cn/" name="AnchorAddByWCM">市场</a>的电子商务重点工作和项目。</p>
								<p align="justify">　<strong>　专项资金发放方式有哪些？</strong></p>
								<p align="justify">　　采取无偿补助、贷款贴息、以奖代补、股权投资及其他方式进行支持，每个项目单位原则上只能选择其中一种支持方式。专项资金贷款贴息的额度，按照项目贷款额度及人民银行公布的同期贷款基准利率按比例确定。对当年已获财政资金支持的同类项目，专项资金不再予以安排。</p>
								<p align="justify"><strong>　　专项资金使用方向？</strong></p>
								<p align="justify">　　办法规定，对获得国家、省、市级“电子商务示范企业”给予奖励；</p>
								<p align="justify">　　鼓励企业自建平台或第三方电子商务平台做大做强，对我市影响力大，对电子商务发展起引导、带动作用的龙头电子商务平台，对电子商务平台的设备购置、系统开发等活动给予补助；</p>
								<blockquote>　　对技术领先、发展前景良好、正常运营的社区生活服务类电子商务平台，优先给予补助；</blockquote>
								<p align="justify">　　对在临沂当地注册的，由本地政府或企业独资或控股，具有独立的第三方支付牌照且结算中心总部设在临沂的第三方支付平台的设备购置、系统开发、应用推广、物流信息发布及<a title="null" target="_blank" href="http://www.langya.cn/" name="AnchorAddByWCM">人才</a>队伍建设等活动给予支持；</p>
								<p align="justify">　　鼓励电子商务企业使用保税仓库、边境仓及海外仓库开展跨境电商业务；支持跨境贸易电子商务企业投保出口信用保险。</p>
								<p align="justify">　　以及鼓励开展电子商务产业集聚区建设，在农业特色产品集中、优势明显、农产品网上经营良好的村(社区)中择优开展“电商村”试点建设并给予支持，鼓励电子商务企业加大资金投入扩大经营规模等。</p>
